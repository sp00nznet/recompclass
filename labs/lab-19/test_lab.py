# ---------------------------------------------------------------------------
#  Main
# ---------------------------------------------------------------------------

if __name__ == "__main__":
    print("NID Resolver -- Test Suite\n")

    test_nid_computation()
    test_nid_known_values()
    test_database_load()
    test_database_lookup()
    test_database_verification()

    print(f"\n{tests_passed} / {tests_run} tests passed.")
    sys.exit(0 if tests_passed == tests_run else 1)
